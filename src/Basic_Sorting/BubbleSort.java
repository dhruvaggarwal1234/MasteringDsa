package Basic_Sorting;



public class BubbleSort {

    static int[]  bubbleSort(int[] arr){
        int n = arr.length ;

        for (int i= 0 ; i<n-1;i++){
            boolean swapped = false;

            for(int j =0; j<n-i-1;j++){
                if(arr[j]>arr[j+1]){
                    int temp = arr[j];
                    arr[j]= arr[j+1];
                    arr[j+1]= temp;
                    swapped=true;
                }
            }
            if(!swapped) break;
        }


        return arr;
    }

    public static void main(String[] args) {

        int [] arr = {1,2,3,4,5};
        bubbleSort(arr);
        for(int a :arr){
            System.out.println(a+ " ");
        }
    }
}
